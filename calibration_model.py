def validate_against_clinical_ranges(predictions: np.ndarray,
                                      method: str = "anatomical") -> dict:
    """
    Compare predicted angles against Mendeley-derived clinical ranges.
    Returns proportion of predictions in each clinical category.
    """
    ranges = config.CLINICAL_RANGES.get(
        "kyphosis" if method == "anatomical" else "geometric",
        config.CLINICAL_RANGES["kyphosis"]
    )
    n      = len(predictions)
    result = {}
    for cat, (lo, hi) in ranges.items():
        pct = float(np.sum((predictions >= lo) & (predictions < hi)) / n * 100)
        result[cat] = pct
        print(f"    {cat}: {pct:.1f}% of frames")
    return result


