# RANDOM FOREST CALIBRATION MODEL
# ────────────────────────────────────────────────────────────────────

class RandomForestCalibrator:
    """
    Baseline calibration model using RandomForest regression.

    Learns: (spinepose, geometric, diff, lean, conf, variance) -> corrected angle
    """

    def __init__(self):
        cfg = config.CALIBRATION_CONFIG
        self.model    = RandomForestRegressor(
            n_estimators=cfg["n_estimators"],
            random_state=cfg["random_state"],
            n_jobs=-1,
        )
        self.scaler    = StandardScaler()
        self.features  = cfg["features"]
        self.target    = cfg["target"]
        self.trained   = False
        self.metrics   = {}

    def fit(self, df: pd.DataFrame):
        """Train the calibrator on feature DataFrame."""
        X = df[self.features].values
        y = df[self.target].values

        X_train, X_test, y_train, y_test = train_test_split(
            X, y,
            test_size=config.CALIBRATION_CONFIG["test_size"],
            random_state=config.CALIBRATION_CONFIG["random_state"],
        )

        X_train_s = self.scaler.fit_transform(X_train)
        X_test_s  = self.scaler.transform(X_test)

        self.model.fit(X_train_s, y_train)
        y_pred = self.model.predict(X_test_s)

        self.metrics = {
            "MAE":         float(mean_absolute_error(y_test, y_pred)),
            "STD_residual":float(np.std(y_test - y_pred)),
            "R":           float(np.corrcoef(y_test, y_pred)[0, 1]),
            "n_train":     len(X_train),
            "n_test":      len(X_test),
        }
        self.trained = True
        self._print_metrics()
        return self

    def predict(self, df: pd.DataFrame) -> np.ndarray:
        """Return corrected angle predictions."""
        if not self.trained:
            raise RuntimeError("Model not trained. Call .fit() first.")
        X = self.scaler.transform(df[self.features].values)
        return self.model.predict(X)

    def feature_importance(self) -> dict:
        """Return feature importance dict."""
        if not self.trained:
            return {}
        return dict(zip(self.features, self.model.feature_importances_))

    def save(self, label: str = "rf_calibrator"):
        path = os.path.join(config.MODEL_DIR, f"{label}.joblib")
        joblib.dump({"model": self.model, "scaler": self.scaler}, path)
        print(f"  Model saved -> {path}")

    def load(self, label: str = "rf_calibrator"):
        path = os.path.join(config.MODEL_DIR, f"{label}.joblib")
        obj  = joblib.load(path)
        self.model   = obj["model"]
        self.scaler  = obj["scaler"]
        self.trained = True
        return self

    def _print_metrics(self):
        m = self.metrics
        print(f"\n  [RandomForest Calibrator] Evaluation:")
        print(f"    MAE:             {m['MAE']:.3f} deg")
        print(f"    STD (residuals): {m['STD_residual']:.3f} deg")
        print(f"    Correlation R:   {m['R']:.4f}")
        print(f"    Train / Test:    {m['n_train']} / {m['n_test']} samples")


# ────────────────────────────────────────────────────────────────────
# MLP DEEP CALIBRATION MODEL (PyTorch)
# ────────────────────────────────────────────────────────────────────

try:
    import torch
    import torch.nn as nn
    from torch.utils.data import DataLoader, TensorDataset
    TORCH_AVAILABLE = True
except ImportError:
    TORCH_AVAILABLE = False


class _MLPNet(nn.Module if TORCH_AVAILABLE else object):
    """Small MLP: 6 features -> 64 -> 32 -> 1."""
    def __init__(self, in_features: int = 6):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(in_features, 64),
            nn.ReLU(),
            nn.Dropout(0.2),
            nn.Linear(64, 32),
            nn.ReLU(),
            nn.Linear(32, 1),
        )

    def forward(self, x):
        return self.net(x).squeeze(1)


class MLPCalibrator:
    """
    Deep calibration model (PyTorch MLP).
    Requires PyTorch. Falls back to RandomForestCalibrator if unavailable.
    """

    def __init__(self, epochs: int = 100, lr: float = 1e-3, batch_size: int = 32):
        if not TORCH_AVAILABLE:
            raise ImportError("PyTorch not installed. Use RandomForestCalibrator instead.")
        self.epochs     = epochs
        self.lr         = lr
        self.batch_size = batch_size
        self.scaler     = StandardScaler()
        self.features   = config.CALIBRATION_CONFIG["features"]
        self.target     = config.CALIBRATION_CONFIG["target"]
        self.model      = None
        self.metrics    = {}

    def fit(self, df: pd.DataFrame):
        X = df[self.features].values.astype(np.float32)
        y = df[self.target].values.astype(np.float32)

        X_train, X_test, y_train, y_test = train_test_split(
            X, y,
            test_size=config.CALIBRATION_CONFIG["test_size"],
            random_state=config.CALIBRATION_CONFIG["random_state"],
        )
        X_train = self.scaler.fit_transform(X_train).astype(np.float32)
        X_test  = self.scaler.transform(X_test).astype(np.float32)

        self.model = _MLPNet(in_features=X_train.shape[1])
        optimizer  = torch.optim.Adam(self.model.parameters(), lr=self.lr)
        loss_fn    = nn.MSELoss()

        ds     = TensorDataset(torch.tensor(X_train), torch.tensor(y_train))
        loader = DataLoader(ds, batch_size=self.batch_size, shuffle=True)

        self.model.train()
        for epoch in range(self.epochs):
            for xb, yb in loader:
                optimizer.zero_grad()
                loss = loss_fn(self.model(xb), yb)
                loss.backward()
                optimizer.step()
            if (epoch + 1) % 20 == 0:
                print(f"    Epoch {epoch+1}/{self.epochs}  loss={loss.item():.4f}")

        # Evaluation
        self.model.eval()
        with torch.no_grad():
            y_pred = self.model(torch.tensor(X_test)).numpy()
        self.metrics = {
            "MAE":          float(mean_absolute_error(y_test, y_pred)),
            "STD_residual": float(np.std(y_test - y_pred)),
            "R":            float(np.corrcoef(y_test, y_pred)[0, 1]),
        }
        print(f"\n  [MLP Calibrator] MAE={self.metrics['MAE']:.3f}  "
              f"R={self.metrics['R']:.4f}")
        return self

    def predict(self, df: pd.DataFrame) -> np.ndarray:
        X = torch.tensor(
            self.scaler.transform(df[self.features].values).astype(np.float32)
        )
        self.model.eval()
        with torch.no_grad():
            return self.model(X).numpy()

    def save(self, label: str = "mlp_calibrator"):
        path = os.path.join(config.MODEL_DIR, f"{label}.pt")
        torch.save(self.model.state_dict(), path)
        joblib.dump(self.scaler, os.path.join(config.MODEL_DIR, f"{label}_scaler.joblib"))
        print(f"  MLP saved -> {path}")


